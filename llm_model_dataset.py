#load our dataset
from datasets import load_dataset
dataset=load_dataset("json",data_files="train_data.json")
print(dataset)

def formatting(data):
  return {
          "text": f"""### Question:{data["Question"]}
                  ### Answer:{data["Answer"]}"""
          }

# Apply the formatting directly to the dataset, as it is already a Dataset object
dataset = dataset.map(formatting)
print(dataset)

from transformers import AutoTokenizer
model_name = "TinyLlama/TinyLlama-1.1B-Chat-v1.0"
tokenizer = AutoTokenizer.from_pretrained(model_name)
tokenizer.pad_token = tokenizer.eos_token

def tokenize(prompt):
    result = tokenizer(
        prompt["text"],
        padding="max_length",
        truncation=True,
        max_length=128
    )
    result["labels"] = result["input_ids"].copy()
    return result

dataset = dataset.map(tokenize, batched=True)
print(dataset)

from peft import LoraConfig, get_peft_model
from transformers import AutoModelForCausalLM # Added import for AutoModelForCausalLM

# Initialize the base model (assuming model_name is already defined in kernel state)
model = AutoModelForCausalLM.from_pretrained(model_name)

config=LoraConfig(r=8,lora_alpha=16,
                  target_modules=["q_proj","v_proj"],
                  lora_dropout=0.1,bias="none",task_type="CAUSAL_LM")
model=get_peft_model(model,config)
model.print_trainable_parameters()
data_collator = DataCollatorForLanguageModeling(tokenizer, mlm=False)

from transformers import TrainingArguments

training_args=TrainingArguments(
    output_dir="./outputs",
    num_train_epochs=5,
    per_device_train_batch_size=1,
    learning_rate=2e-4,
    logging_steps=1,
    save_strategy="epoch",
    fp16=True
)

from transformers import Trainer

trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=dataset["train"],
    data_collator=data_collator
)
trainer.train()

model.save_pretrained("tiny_llama_adapter")
tokenizer.save_pretrained("tiny_llama_adapter")

